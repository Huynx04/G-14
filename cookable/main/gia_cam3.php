                <h6>Cách làm:</h6>
                <p>1. Mề gà làm sạch, thái miếng vừa ăn. Ướp mề gà với bột gia vị hoặc nước tương, dầu hào và chút tiêu. Để khoảng 20-30 phút. Hành tỏi bóc vỏ, bằm nhuyễn (để riêng hành và tỏi). Húng quế rửa sạch, nhặt lấy phần lá.</p>
                <img src="https://nguyentuanbao.com/wp-content/uploads/2022/05/gion-thom-me-ga-xao-hung-que-2.jpg" alt="Mề gà xào húng quế"><br>
                <p>2. Đun nóng 1 thìa dầu ăn trong chảo. Để lửa vừa – to, cho hành vào phi thơm. Cho mề gà đã ướp vào xào ở lửa to. Có thể nêm lại gia vị cho vừa ăn, nếu cảm thấy trong chảo quá khô thì cho thêm 1-2 thìa nước nhỏ. Khi mề gà vừa chín tới thì tắt bếp. Cho tỏi băm và húng quế vào đảo đều. Hơi nóng trong chảo sẽ giúp húng quế “chín” mà không bị nát. Rắc thêm hạt tiêu. Dùng nóng với cơm trắng.</p>
                <img src="https://nguyentuanbao.com/wp-content/uploads/2022/05/gion-thom-me-ga-xao-hung-que-6.jpg" alt="Mề gà xào húng quế"><br>
                <h7>Yêu cầu thành phẩm:</h7><br>
                <p>Mề gà giòn, đậm đà vừa ăn (khi xào mình thường hay dùng dầu hào để giúp món ăn có độ “bóng” và có chút ngọt rất dễ chịu, rất khác với kiểu ngọt của cho đường), thơm mùi tỏi và húng quế.</p>

