                    <h6>Nguyên liệu:</h6>
                    <h8>300gram mề gà (hoặc thịt gà lọc xương)</h8><br>
                    <h8>2-3 tép tỏi (tỏi bên này thì dùng 1 tép to)</h8><br>
                    <h8>1 thìa cafe bột gia vị (mình dùng gia vị Hải Châu, các bạn có thể thay bằng muối hoặc xì dầu/ nước tương tùy ý)</h8><br>
