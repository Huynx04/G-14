      <h2>Cách làm</h2> 
        <p><span style="color: #000000;">1. Rửa và xát sạch đùi vịt bằng rượu trắng và gừng khoảng 2 – 3 lần để khử mùi hôi, để ráo.</span></p>
        <p><span style="color: #000000;">2. Hành khô bóc vỏ, nướng xém vàng. Xương lợn rửa sạch, cho vào nồi ngập nước, đun sôi khoảng 1 phút cho ra hết cặn bẩn, rửa lại rồi cho lên bếp ninh cùng khoảng 4 bát tô nước và hành khô đã nướng trong 30 phút.</span></p>
        <p><span style="color: #000000;">3. Trong khi đợi ninh nước, chuẩn bị các nguyên liệu khác:</span></p>
        <p><span style="color: #000000;">&#8211; Nấm hương ngâm với nước ấm trong 15 – 20 phút cho nở mềm. Vớt ra rửa sạch, cắt bỏ chân.</span></p>
        <p><span style="color: #000000;">&#8211; Rau cải tách lá, rau mùi bỏ rễ, rửa sạch.</span></p>
        <p><span style="color: #000000;">&#8211; Gừng và giềng cạo vỏ, thái lát. Sả cắt gốc, bỏ lớp vỏ ngoài, cắt khúc 6 -7 cm rồi chẻ dọc làm 2 – 3 phần.</span></p>
        <p><span style="color: #000000;">&#8211; Hoa hồi, quế, thảo quả, trần bì, thục địa rửa sạch, để ráo. Dùng dao gọt bớt phần cùi trắng bên trong của trần bì để không làm đắng nước dùng. Thảo quả đập dập.</span></p>
        <p><span style="color: #000000;">4. Cho 15 grams đường vào nồi cùng 20 ml nước, đun lửa nhỏ tới khi đường chảy ra và nước đường chuyển vàng nâu thì nhấc xuống (có cần đun tới khi thành nước hàng?). Cho 15 ml xì dầu vào nồi rồi đun sôi trở lại, nước đường và xì dầu hoà quyện. Để hỗn hợp nguội bớt tới khi chỉ còn hơi ấm.</span></p>
        <p><span style="color: #000000;">Ướp vịt với hỗn hợp nước xì dầu trong khoảng 15 phút cho ngấm và giúp vịt lên màu đẹp khi chiên.</span></p>
        <p><span style="color: #000000;">5. Làm nóng trong chảo chống dính một lượng dầu vừa đủ ngập nửa miếng thịt, cho gừng, riềng và sả vào chiên tới khi xém vàng thì vớt ra. Cho vịt đã ướp vào chiên vàng đều các mặt, gắp ra đĩa lót giấy cho thấm dầu.</span></p>
        <p><span style="color: #000000;">6. Cho vịt đã chiên và nấm hương, thục địa, trần bì vào nồi nước hầm xương, đun sôi trở lại rồi vặn nhỏ đun liu riu thêm khoảng 20 – 30 phút nữa cho thịt vịt mềm nhừ, có thể thêm nước nếu cần.</span></p>
        <p><span style="color: #000000;">7. Tiếp tục thả 4 cái hoa hồi, 1 thanh quế nhỏ (khoảng 2 – 3 g), 1 thảo quả vào nồi. Có thể rang qua các vị thuốc bắc này trước khi thả vào nồi để mùi vị được thơm hơn. Nêm bột gia vị/ muối, đường và tiêu cho vừa khẩu vị.</span></p>
        <p><span style="color: #000000;">8. Rau cải trần với nước sôi và chút muối trong 2 – 3 phút rồi vớt ra thả vào tô nước lạnh để giữ rau luôn đc xanh. Tiếp tục dùng nồi nước đó trụng mì trứng, không để mì chín quá vì khi chan với nước dùng mì sẽ nở và chín thêm.</span></p>
        <p><span style="color: #000000;">9. Chia mì ra các bát tô, xếp rau cải, nấm hương, rau mùi lên trên, chan nước dùng và thưởng thức.</span></p>
        <p><span style="color: #000000;">*Yêu cầu thành phẩm: thịt vịt đậm đà không còn mùi hôi, mềm nhừ. Nước dùng thơm nhẹ mùi các vị thuốc bắc, vị ngọt vừa phải.</span></p>
       
