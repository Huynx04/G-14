      <h2>Cách làm</h2> 
      <p>1. Vịt rửa sạch. Đập dập ít gừng rồi pha với rượu trắng, dùng rượu gừng này xát đều bên trong và ngoài con vịt, để khoảng 20-25 phút cho hết hôi. Rửa sạch bằng nước lạnh rồi thấm thật khô cả trong lẫn ngoài. Nếu da vịt có chỗ nào bị rách thì nên dùng chỉ trắng khâu lại, da càng căng thì khi nướng càng ngon hơn.</p>
      <p>2. Trộn đều hỗn hợp gia vị ướp bên trong. Định lượng gia vị có thể thay đổi tùy khẩu vị của gia đình, sau khi trộn các bạn có thể nếm thử để điều chỉnh. Dùng tay xát phần gia vị này vào khắp bên trong con vịt. Phần bên ngoài vịt các bạn trộn 2/3 thìa cafe muối với 2/3 thìa cafe ngũ vị hương. Xát đều lên da vịt.</p>
      <p>Nếu có thể treo vịt lên cao để vịt khô trong không khí là tốt nhất. Còn không thì các bạn để vịt lên giá có lỗ hoặc khe hở, kê giá lên cao. Để ở nơi thoáng gió cho vịt thật khô. Ướp tối thiểu 6-8 tiếng (để qua đêm).</p>
      <p>3. Cho hỗn hợp gia vị ướp bên ngoài vào nồi, gồm: Mạch nha, nước sôi, xì dầu (nước tương), (bột) gừng, (bột) tỏi, dấm và muối. Tất cả các nguyên liệu này đều có tác dụng giúp cho da vịt thơm ngon, có màu vàng nâu bóng và giòn nên các bạn cố gắng tìm đủ nguyên liệu nhé. Riêng phần nước sôi, chỉ cần cho vừa đủ để giúp mạch nha dễ tan, <em>không cho quá nhiều,</em> hỗn hợp loãng sẽ khó dội lên vịt.</p>
      <p>Đun cho hỗn hợp này sôi thật mạnh. Hỗn hợp sau khi đun sẽ hơi sánh, có mùi chua nhẹ của dấm, vị mặn ngọt vừa phải, và rất thơm. Dùng hỗn hợp này dội lên bên ngoài con vịt. <em>Nên dội nhiều lần và dội thật kĩ lên khắp mình v</em>ịt (cả những chỗ như bên trong cánh, bên trong đùi&#8230;).<em> Càng dội kĩ thì vịt sau này quay sẽ càng bóng đẹp và ngon</em>. Nếu hỗn hợp nguội trong quá trình làm thì các bạn có thể đun lại cho sôi rồi dội tiếp.</p>
      <p>Tiếp tục treo hoặc để con vịt trên giá cho khô. Tùy vào việc trời có khô ráo hay không mà thời gian phơi sẽ dao động trong khoảng từ 2-3 giờ cho đến 7-8 giờ. Có thời gian để được càng lâu thì càng ngon.</p>
      <ul class="inside">
      <li><span style="text-decoration: underline;"><strong>Lưu ý</strong></span>: <span style="color: #ff0000;">nếu thời tiết nóng ẩm thì nên để vịt trong tủ lạnh, nếu không ngại mùi, có thể mở vịt ra thì tốt, còn nếu không thì có thể bọc kín vịt.</span></li>
      </ul>
      <p>4. Chuẩn bị quay vịt. Ngon nhất có lẽ là có lò quay chuyên dụng, có thể xiên vịt và quay vòng vòng thì vịt sẽ chín đều. Nướng bằng lò thì sẽ phải lật cho vịt chín đều, nhưng vẫn rất thơm ngon.</p>
      <p>Trong quá trình quay sẽ có rất nhiều mỡ chảy ra từ vịt, cho nên các bạn <em>nên chuẩn bị sẵn một chiếc khay đặt ở dưới để hứng phần mỡ này</em> (nếu không chắc sẽ không bao giờ dám quay vịt lần nữa vì dọn lò mệt lắm). Hoặc có thể chuẩn bị sẵn một ít rau củ như cà rốt, bí ngòi, cà chua, để ở dưới. Rau được nướng với mỡ vịt sẽ rất thơm ngon :)</p>
      <p><a title="IMG_8470 by Uness, on Flickr" href="http://www.flickr.com/photos/55490207@N05/8287204733/"><img loading="lazy" class="aligncenter" src="http://farm9.staticflickr.com/8495/8287204733_a32686d355.jpg" alt="IMG_8470" width="500" height="333" /></a></p>
      <p>5. Cho vịt vào lò (đặt vịt trên rack đi kèm lò, khay hứng mỡ hoặc khay rau củ để ở dưới). Nướng ở nhiệt độ trong khoảng 160-170 độ C. Với con vịt 1kg thì mình nướng trong khoảng 45-55 phút (cho vịt vào mới bật lò). Sau 20-25 phút đầu tiên thì lật vịt cho vịt chín đều.  Sau khi vịt đã chín, các bạn có thể để vịt ở trong lò (đã tắt) thêm khoảng phút cho phần mỡ chảy ra hết, da vịt khô lại.</p>
      <p><a title="IMG_8484 by Uness, on Flickr" href="http://www.flickr.com/photos/55490207@N05/8287202085/"><img loading="lazy" class="aligncenter" src="http://farm9.staticflickr.com/8486/8287202085_d15d302b0a.jpg" alt="IMG_8484" width="500" height="333" /></a></p>
      <p>Vịt nướng đạt sẽ có phần da giòn, vàng nâu bóng, mằn mặn ngòn ngọt (và rất thơm), thịt hơi hồng và mềm mại, không khô, không dai (tả đến đây thôi nhé, không có nước miếng ướt hết cả bàn phím rùi :&#8221;&gt; :&#8221;&gt;)</p></span></p>
       
