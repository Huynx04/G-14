                <p><span style="color: #000000;">&#8211; Lưu ý với món ăn này các bạn nên dùng với cơm nấu khô một chút, vì lượng nước từ thịt và trứng sẽ chảy xuống làm cơm mềm ra.</span></p>
                <p><span style="color: #000000;">&#8211; Thịt gà rửa sạch, lọc xương và da nếu có, thấm khô. Thái thịt thành những miếng vừa ăn dày 1 – 1,5 cm.</span></p>
                <p><span style="color: #000000;">&#8211; Ướp thịt với ½ thìa café muối và ½ thìa café tiêu xay, để riêng trong 15 – 20 phút cho ngấm gia vị.</span></p>
                <p><span style="color: #000000;">&#8211; Hành tây bóc vỏ, bổ đôi, thái lát. Rau mùi tây nhặt rễ, rửa sạch, thái nhỏ.</span></p>
                <p><span style="color: #000000;">&#8211; Đập 1 quả trứng ra bát, quậy đều.</span></p>
                <p><span style="color: #000000;">2. </span><span style="color: #000000;">Hòa tan các nguyên liệu để làm nước dùng gồm: 80ml dashi, 20ml mirin, 20ml xì dầu, 5 grams đường.</span></p>
                <p><span style="color: #000000;">3. Rải đều chỗ hành tây thái lát lên mặt chảo. Rót nước dùng lượng vừa đủ để ngập mặt thịt gà và hành tây (không cần dùng hết lượng nước đã pha)</span></p>
