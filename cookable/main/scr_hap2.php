        <h2>Cách làm</h2>      
<p><span style="color: #000000;">Đặt thịt gà lên trên, nên phủ đều khắp mặt chảo để thịt gà được chín đều. </span></p>
<p><img loading="lazy" class="aligncenter size-large wp-image-8417" src="https://toquoc.mediacdn.vn/280518851207290880/2023/4/10/z4242057357829e5aa3137470a9a6828439cf969196160-16811161023701761259101.jpg" alt="" width="1024" height="691" sizes="(max-width: 1024px) 100vw, 1024px" /></p>
<p><span style="color: #000000;">Đậy vung đun lửa nhỏ vừa tới khi thịt gà chín tái. * </span>Lưu ý: hành và thịt gà chỉ dàn 1 lớp trên mặt chảo, không xếp chồng lên nhau. Vì thế các bạn có thể chia làm 2 mẻ nếu chảo nhỏ.</p>
<p><span style="color: #000000;">4. Rót từ từ trứng vào chảo sao cho thịt và hành tây được phủ đều trong trứng. </span></p>
<p><span style="color: #000000;">Đậy vung đun lửa nhỏ vừa trong khoảng 5 &#8211; 7 phút tới khi thịt gà và trứng vừa chín tới.</span></p>
<p><span style="color: #000000;">Lưu ý: Độ chín vừa mềm (trong chảo vẫn còn nước dùng) hay chín kĩ của trứng phụ thuộc vào sở thích của gia đình bạn. Tuy nhiên không nên để trứng quá chín kỹ (nước dùng bị cạn hết) sẽ bị khô, khi ăn với cơm không còn độ mềm mượt.</span></p>
<p><img loading="lazy" class="aligncenter size-large wp-image-8420" src="https://yummyday.vn/uploads/images/cach-chien-trung-voi-thit-bam.jpg" alt="" width="1024" height="729"  sizes="(max-width: 1024px) 100vw, 1024px" /></p>
<p><span style="color: #000000;">5. Rắc rau mùi tây lên mặt trứng ngay trước khi tắt bếp. Nhẹ nhàng chuyển trứng phủ lên mặt bát cơm nóng (có thể dùng phới xúc ra, hoặc đặt chảo sát vào mặt bát cơm, nghiêng chảo cho trứng tự trôi ra).</span></p>
<p><span style="color: #000000;">Tiếp tục lặp lại thao tác với quả trứng và số hành tây, thịt gà còn lại. Dùng nóng khi trứng và cả thịt gà đều còn đang rất mềm và thơm ngon :) </span></p>
