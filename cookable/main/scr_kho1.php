       <div id="main">  
        <h2>Cách làm</h2>   
        <p>1. Rửa sạch tôm khô, ngâm vào nước sôi tối thiểu 20 phút cho tôm mềm.</p>
        <p>2. Lọc bỏ phần da, thái riêng phần mỡ và nạc thành cỡ hạt lựu, để riêng hai phần. Nếu không tách được riêng mỡ và nạc thì thái chung thành cỡ hạt lựu cũng không sao.</p>
        <p>Hành, tỏi bóc vỏ, thái lát mỏng và băm nhỏ (tỏi).</p>
        <p>3. Để lửa vừa, rang phần thịt mỡ trong chảo chống dính để mỡ tiết ra từ từ. Nếu mỡ có dấu hiệu bị cháy nhanh và xát chảo thì thêm vào chảo chút dầu ăn. Đun nhỏ lửa, thi thoảng đảo cho mỡ cháy đều các cạnh. Khi mỡ đã tiết ra tương đối nhiều và các miếng mỡ phần teo lại thành tóp mỡ nâu vàng thì vớt phần tóp mỡ này ra để riêng.</p>
        <p>4. Để lửa vừa-to, cho hành tỏi vào phi thơm. Tiếp theo cho tôm và thịt vào đảo cho săn lại (nếu không tách riêng hai phần thịt và mỡ thì rang tất cả thịt trong bước (3) nhưng không nên rang quá lâu, sẽ làm thịt bị cứng).</p>
        <p>5. Pha hỗn hợp nước mắm theo tỉ lệ 1 đường: 1.5 &#8211; 2 nước mắm: 1.5 nước. VD 2 thìa đường: 3 &#8211; 4 thìa nước mắm: 3 thìa nước.</p>    
        <p><span style="color: #f71414;"><strong>Lưu ý</strong></span></p>
    <ul>
        <li>Nếu các bạn muốn có thật nhiều mắm để chấm, thịt và tôm chỉ phụ thêm vào thì tăng lượng mắm, đường và nước lên nhưng cần giữ tỉ lệ như trên. Mình không cần quá nhiều nước chấm nên chỉ làm theo lượng như trong phần nguyên liệu</li>
        <li>Mình dùng 1/2 lượng đường là đường nâu để giúp cho món ăn có màu đẹp hơn</li>
        <li>Mình dùng nước ngâm tôm để pha nước chấm này vì món ăn sẽ đậm đà hơn</li>
        <li>Mỗi loại nước mắm có độ mặn khác nhau (đặc biệt là nước mắm mua bên này thường mặn hơn mắm ở VN kha khá). Nên ban đầu có thể cho ít nước mắm một chút, về sau khi nấu thì nếm lại và thêm mắm nếu thấy nhạt.</li>
    </ul>
        <p>6. Đổ bát nước mắm này vào chảo, kèm theo tóp mỡ và ớt. Đảo đều. Đun liu riu trong 5 &#8211; 10 phút để nước cạn từ từ và sệt lại. Nếu thấy nước bay hơi quá nhanh thì cho thêm ít nước. Nên đun từ từ để tôm thịt thật ngấm mắm đường và không bị cứng.</p>
        <p>7. Khi nước đã gần sệt như ý muốn thì nêm nếm lại và cho thêm ớt, tiêu &#8230; tuỳ thích. Cuối cùng cho hành xanh và đảo đều. Tắt bếp.</p>
        <p>Lượng nước mắm đường trong kho quẹt nhiều hay ít hoàn toàn phụ thuộc vào người nấu. Như mình viết trong lưu ý ở trên, các bạn có thể thay đổi lượng mắm, đường, nước tuỳ theo nhu cầu, miễn là giữ đúng tỉ lệ. Món kho quẹt &#8220;gốc&#8221; thật ra chỉ có mắm, đường, tí nước chưng với mỡ và tóp mỡ thôi, nhưng giờ được &#8220;nâng cấp&#8221; có cả tôm với thịt nữa.</p>
        <p>Kho quẹt nấu đạt sẽ có mùi rất thơm, mặn ngọt vừa phải, tôm thịt không quá cứng và mỡ giòn (nếu thích ăn tóp mỡ rất giòn thì cho tóp mỡ vào ngay trước khi bắc khỏi bếp). Dùng để chấm với rau luộc, dưa chuột hay các loại salad, trộn với cơm trắng, xôi trắng, ăn kèm cơm cháy đều rất ngon :)</p>
