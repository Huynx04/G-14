        <h2>Cách làm</h2>      
        <ul class="inside">
            <p><span style="color: #000000;">1. Sơ chế nguyên liệu:</span></p>
        <li><span style="color: #000000;">Mộc nhĩ, nấm hương ngâm nở, rửa sạch, thái chỉ</span></li>
        <li><span style="color: #000000;">Cà rốt gọt vỏ, nạo sợi</span></li>
        <li><span style="color: #000000;">Đậu que tước vỏ sơ, rửa sạch, để nguyên quả</span></li>
        <li><span style="color: #000000;">Lạp xưởng cắt làm 4, giữ nguyên độ dài</span></li>
        <li><span style="color: #000000;">Trứng muối lấy lòng đỏ, rửa qua với rượu để khử tanh, thấm khô, hấp chín hoặc nướng 5’ trong lò nhiệt độ 170 độ C.</span></li>
        <li><span style="color: #000000;">Lá dong rửa sạch, lau khô, tước bỏ phần cứng giữa lá.</span></li>
        <img src="https://cdn.tgdd.vn/2021/03/content/35-800x533-800x450.jpg" alt="">

    </ul>
        <p><span style="color: #000000;">2. Cho các nguyên liệu: giò sống, mộc nhĩ, nấm hương thái chỉ, cà rốt nạo sợi vào tô to. Ướp vào tô 1 thìa cafe đường, 1 thìa cafe nước mắm, 1 thìa cafe hạt tiêu, trộn thật đều.</span></p>
        <p><span style="color: #000000;">3. Đánh tan 3 quả trứng với 1 thìa canh nước lọc, nêm vào ½ thìa cafe gia vị. Chia trứng làm 2 phần tráng mỏng.</span></p>
        <p><span style="color: #000000;">4. Trải 1 miếng trứng xuống mặt thớt to, dàn đều ½ hỗn hợp giò sống khắp mặt trứng, lần lượt xếp đậu que, rồi đến lạp xưởng, sau đó xếp 5 lòng đỏ trứng muối thẳng hàng. Tiếp tục xếp lạp xưởng và đậu que lần lượt, để cho sau khi cuộn thì trứng muối sẽ ở chính giữa lạp xưởng và đậu que.</span></p>
        <p><span style="color: #000000;">5. Cẩn thận cuộn trứng tròn lại, sao cho toàn bộ nhân nằm gọn bên trong</span></p>
        <p><span style="color: #000000;">6. Đặt 2 lá dong cạnh sát nhau theo chiều dọc, rồi đặt tiếp 2 lá lên trên nằm ngang. Đặt cuộn trứng vào đầu tấm trải lá dong, cẩn thận cuộn tròn thật kín. Dùng lạt buộc để lá dong không bị bung trong khi hấp.</span></p>
        <img src="https://cdn.tgdd.vn/2021/03/CookRecipe/GalleryStep/cha-gio-tom-thit-khoai-mon-1.jpg" alt="">
        <p><span style="color: #000000;">7. Làm tương tự đến bước 5 với cây giò thứ 2. Nếu không có lá dong, bạn có thể cuốn chặt giò bằng màng bọc thực phẩm, xoắn chặt 2 đầu lại. Sau đó cuốn thêm 1 lớp giấy bạc ra bên ngoài, xoắn chặt 2 đầu giấy bạc là được.</span></p>
        <p><span style="color: #000000;">8. Hấp cách thủy trong vòng 45’, để nguội, bóc vỏ rồi thái khoanh tròn.</span></p>
        <p><span style="color: #000000;">9. Yêu cầu thành phẩm: Giò và các loại nhân chín đều, dai dai giòn giòn. Có thể ăn như các món nguội, chấm thêm nước mắm tiêu nếu muốn đậm đà hơn.</span></p>
