        <h2>Cách làm</h2>      
<p><strong><span style="color: #000000;">A. Phần nhân tôm</span></strong></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Tôm sau khi đã lột vỏ và bỏ đầu, trộn với ½ thìa cafe muối và để nghỉ trong 5 phút. Bước này sẽ giúp loại bỏ chất nhờn ở ngoài da tôm, sau khi hấp lên tôm vẫn giữ được độ giòn và “tươi” hơn nữa.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Sau 5 phút, rửa lại tôm với nước sạch. Thái sơ tôm, lưu ý không băm quá nhỏ.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Mỡ phần rửa sạch. Đun sôi một nồi nước, thả mỡ vào luộc trong khoảng 3 phút đến khi mỡ cứng lại. </span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Vớt mỡ ra thả vào nước lạnh, sau đó thái hạt lựu nhỏ.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Củ đậu gọt vỏ, thái hạt lựu vỡ bằng mỡ phần.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Cho tôm đã thái nhỏ vào bát với 1/2 thìa cafe muối, 1 thìa cafe đường, ⅛ thìa cafe hạt tiêu, 1 thìa cafe bột ngô. Dùng đũa đảo tôm theo 1 chiều trong khoảng 5 phút, đến khi tất cả gia vị hòa quyện, tôm trở nên dẻo và dính hơn.</span></p>
<p><span style="color: #000000;">&#8211; Trộn mỡ phần và củ đậu đã thái nhỏ vào bát tôm, trộn đều. Bọc kín âu và để và tủ lạnh trong khoảng 30 phút. Trong thời gian ấy chúng ta sẽ đi làm vỏ há cảo.</span></p>
<div class="inside"><strong><span style="color: #000000;">B. Phần vỏ</span></strong></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Trộn đều bột tàn mì, bột năng và muối. Đổ nước sôi vào và dùng đũa đảo đều.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Bọc kín âu, để nghỉ trong khoảng 10 phút cho bột chín và nguội bớt.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Sau 10 phút, cho vào bát bột 15 ml (1 thìa canh) dầu ăn. Dùng tay nhào bột trong khoảng 5 phút đến khi ta được hỗn hợp mịn dẻo.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Chia bột thành các phần nặng 10 &#8211; 12 gram, bọc kín để bột không bị khô. </span></p>
</div>
<p><strong><span style="color: #000000;">C. Tạo hình</span></strong></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Vê tròn mỗi phần bột, cán mỏng. Bột rất mềm, không dính tay, không co và rất dễ cán. </span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Khi cán cố gắng cán 1 phần mỏng hơn để lúc nặn sẽ gấp phần đó.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Cho lượng nhân bằng khoảng 1 thìa canh vào vỏ bánh đã cán. Nhẹ nhàng gấp phần mép mỏng hơn vào mép còn lại. Cách làm cụ thể trong video.</span></p>
<p><span style="font-weight: 400; color: #000000;">Lưu ý: Trong quá trình cán và tạo hình, tất cả những phần bột chưa dùng đến phải được che kín để tránh bị khô. Nếu bột khô khi hấp sẽ rất dễ vỡ.</span></p>
<div class="inside">><strong><span style="color: #000000;">D. Hấp bánh</span></strong></p>
<p><span style="color: #000000;">&#8211; Đun sôi nước trong nồi hấp. Phết một lớp dầu ăn mỏng lên đĩa hấp để chống dính.</span></p>
<p><span style="font-weight: 400; color: #000000;">&#8211; Hấp há cảo ở lửa lớn trong 5 phút.</span></p>
<p><span style="font-weight: 400; color: #000000;">Dùng nóng với nước tương.</span></p>
<p><span style="color: #000000;">Chúc các bạn có một nồi há cảo thật ngon!!</span></p>  
