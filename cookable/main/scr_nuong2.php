                <h2>Cách làm</h2>
                <p>1. Tôm cắt bớt phần râu và ngạnh cứng ở đầu. Dùng que tre xiên dọc theo mình con tôm. Dùng mũi dao nhọn hoặc kéo rạch sống lưng, lấy chỉ đen. Rửa sạch và thấm khô (hình 1)</p>
                <p><em>Tôm thì nếu cả nhà chọn được tôm sú to là ngon nhất, ở bên này tôm khá đắt nên mình dùng tôm loại bình thường thôi. Xiên tre cũng không bắt buộc, có thì đẹp hơn :)</em></p>
                <p>2. Trộn tất cả các nguyên liệu còn lại (hình 2). Dùng thìa nhỏ xúc hỗn hợp bơ tỏi này đổ vào phần sống lưng tôm đã rạch sẵn (hình 3). Nếu dùng còn thừa thì có thể phết lên mình tôm.</p>
                <p>3. Xếp tôm lên khay nướng. Nướng ở nhiệt độ 200-210 độ C đến khi tôm chín. Mình cho tôm vào mới bật lò, sau khoảng 25-30 phút thì tôm chín vừa ngon.</p>
                <p>4. Dùng nóng với ít chanh tươi. Ah, nếu có thêm 1 ly vang trắng lạnh nữa thì tuyệt ;)</p>
