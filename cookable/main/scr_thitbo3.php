                <h2>Cách làm</h2>
<p>1. Ướp thịt bò:</p>
<p>&#8211; Thịt bò rửa sạch, dùng khăn sạch hoặc giấy bếp thấm khô. Thái miếng to cỡ 2.5 x 2.5cm. Ướp thịt bò với các gia vị trong phần A. Để tối thiểu 3 tiếng hoặc tốt nhất là ướp qua đêm cho thịt ngấm.</p>
<p><a href=""><img loading="lazy" class="aligncenter" src="https://cdn.tgdd.vn/Files/2020/03/12/1241669/cach-lam-hu-tieu-bo-kho-thom-ngon-tai-nha-5.jpg" alt="IMG_6516" width="880" height="533" /></a></p>
<p>2. Làm sốt cà chua:</p>
<p>&#8211; Đun nóng dầu ăn, phi thơm hành.</p>
<p>&#8211; Cho cà chua (bổ múi cau) vào chảo. Nêm thêm chút muối và đường cho cà chua đậm đà. Đảo đều. Cho vào chảo khoảng 1/2 bát con nước lạnh. Để lửa vừa cho hỗn hợp trong chảo sôi liu riu. Đun trong khoảng 10 &#8211; 15 phút, đến khi cà chua chín mềm nhuyễn có thể dễ dàng dầm nát thì bắc ra khỏi bếp.</p>
<p>&#8211; Lọc cà chua qua rây. Nếu hỗn hợp quá đặc, có thể cho thêm chút nước. Kết quả sau bước này là chúng mình sẽ có nước sốt cà chua nhuyễn mịn. Phần vỏ và hột sẽ được giữ lại trong rây. Các bạn có thể dùng sốt cà chua đóng hộp (loại nguyên chất), nhưng mình nghĩ dùng cà chua tươi mùi vị sẽ ngon hơn.</p>
<p>3. Nấu bò kho:</p>
<p>&#8211; Đun nóng dầu ăn trong chảo, cho hành vào phi thơm.</p>
<p>&#8211; Cho thịt bò và tỏi vào, đảo đều ở lửa to. Khi mặt ngoài miếng thịt vừa săn lại thì cho nước sốt cà chua vào, đảo đều cùng thịt bò (<em>lưu ý, không xào thịt bò quá lâu sẽ làm cho thịt bò dễ bị dai và cứng</em>).</p>
<p><a  href=""><img loading="lazy" class="aligncenter" src="hhttps://cdn.tgdd.vn/Files/2020/03/12/1241669/cach-lam-hu-tieu-bo-kho-thom-ngon-tai-nha-6.jpg" alt="2014-09-25" width="880" height="513" /></a></p>
<p>&#8211; Đổ thêm nước nóng cho ngập thịt bò. Đun sôi, hớt sạch bọt trong nồi. Cho vào nồi sả, hoa hồi, quế. Hạ lửa nhỏ ninh thịt bò chín mềm.</p>
<p class ="inside">* <span style="text-decoration: underline;"><strong>Ghi chú</strong></span>: <em>Hồi quế có mùi khá mạnh, nếu không quen thì không nên cho nhiều, hoặc các bạn có thể bỏ hẳn hồi quế và thêm vào nồi ít bột ngũ vị hương. Có thể rang sơ qua hồi quế cho thơm trước khi bỏ vào nồi. </em></p>
<p>&#8211; Sau khi ninh thịt khoảng 1/2 thời gian thì nêm nếm lại gia vị (mình nêm sau khoảng 30 phút tính từ lúc hạ lửa nhỏ). Nên nêm nếm sau khi đã ninh thịt được một khoảng thời gian vì lúc này thịt bò đã tiết ra nước ngọt, cà chua cũng ngấm vào làm cho nồi nước đậm đà và ngon hơn, nếm cũng dễ chuẩn vị hơn. Mình thêm khoảng 1 thìa cafe bột canh, 1/2 thìa cafe đường và chút bột ngũ vị hương. Các bạn có thể tự điều chỉnh lượng bột canh/ muối và đường cho phù hợp với khẩu vị của gia đình nhé.</p>
<p>Nếu trong quá trình nấu, nước trong nồi cạn bớt thì các bạn có thể thêm nước nóng vào nồi.</p>
<p>&#8211; Trong lúc đợi ninh thịt bò thì thái cà rốt thành miếng vừa ăn. Đợi thịt bò mềm rồi cho cà rốt vào nồi, nấu đến khi thịt và cà rốt mềm nhừ. Mình dùng nồi thường, bếp thường thì ninh mất khoảng 45 &#8211; 55 phút, nếu các bạn dùng nồi áp suất sẽ nhanh hơn.</p>
<p><a href=""><img loading="lazy" class="aligncenter" src="https://cdn.tgdd.vn/Files/2020/03/12/1241669/cach-lam-hu-tieu-bo-kho-thom-ngon-tai-nha-9.jpg" alt="IMG_6526" width="880" height="533" /></a></p>
<p>&#8211; Bò kho có thể ăn với bánh  mì, hủ tiếu (hoặc cơm hay mì tùy thích). Nếu để ăn với hủ tiếu, các bạn có thể thêm nước. Còn nếu muốn dùng với bánh mì thì làm cho nước thịt sánh hơn bằng cách sau: hòa tan khoảng 2 thìa cafe bột mì hay bột năng trong 20 &#8211; 30 ml nước. Khi thịt bò đã chín mềm thì để lửa nhỏ, từ từ đổ hỗn hợp bột hòa tan trong nước vào nồi thịt, vừa đổ vừa quấy đều. Khi thấy nước trong nồi đủ sệt như ý muốn thì dừng lại (không nhất thiết phải dùng hết chỗ nước bột).</p>
<p>* Yêu cầu thành phẩm: Thịt bò chín mềm nhưng không bị bã, ngấm gia vị mặn ngọt đậm đà, nước dùng có màu đỏ sánh đẹp, dậy mùi thịt bò quyện với cà chua và ngũ vị hương.</p>
<p>Chúc cả nhà cuối tuần thật vui và thoải mái! ^.^</p>
